# Iteráveis = Um objeto/coleção que pode retornar seus elementos um de cada vez,
# permitindo que seja percorrido (iterado) em um loop.

numbers = (1, 2, 3, 4, 5)

for number in numbers:
    print(number)


my_dictionary = {"A": 1, "B": 2, "C": 3}

for key, value in my_dictionary.items():
    print(key, value)